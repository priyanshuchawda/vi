const ContentPanel = ({ activeTab, onClose }: ContentPanelProps) => {
  const panelRef = useRef<HTMLDivElement>(null);

  // Close on escape key
  useEffect(() => {
    const handleKeyDown = (e: KeyboardEvent) => {
      if (e.key === 'Escape') {
        onClose();
      }
    };

    window.addEventListener('keydown', handleKeyDown);
    return () => window.removeEventListener('keydown', handleKeyDown);
  }, [onClose]);

  // Close when clicking outside
  useEffect(() => {
    const handleClickOutside = (e: MouseEvent) => {
      if (panelRef.current && !panelRef.current.contains(e.target as Node)) {
        // Check if click is not on the icon sidebar
        const target = e.target as HTMLElement;
        if (!target.closest('[data-icon-sidebar]')) {
          onClose();
        }
      }
    };

    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, [onClose]);

  if (!activeTab) return null;

  const config = tabConfig[activeTab];

  const renderContent = () => {
    switch (activeTab) {
      case 'project':
        return <ProjectTab />;
      case 'media':
        return <MediaTab />;
      case 'text':
        return <TextTab />;
      case 'settings':
        return <SettingsTab />;
      case 'memory':
        return <AIMemoryPanel />;
      case 'youtube':
        return <YouTubeUploadTab />;
      default:
        return <MediaTab />;
    }
  };

  return (
    <>
      {/* Backdrop */}
      <div className="fixed inset-0 bg-black/20 z-40 backdrop-blur-sm" onClick={onClose} />

      {/* Panel */}
      <div
        ref={panelRef}
        className={clsx(
          "fixed left-16 top-0 bottom-0 w-80 bg-bg-secondary border-r border-border-primary z-50",
          "shadow-2xl animate-slide-in flex flex-col"
        )}
      >
        {/* Header */}
        <div className="px-4 py-3 border-b border-border-primary bg-bg-elevated">
          <div className="flex items-center justify-between">
            <div className="flex items-center gap-2">
              <div className="text-accent">{config.icon}</div>
              <h2 className="text-sm font-semibold text-text-primary">{config.title}</h2>
            </div>
            <button
              onClick={onClose}
              className="p-1.5 text-text-muted hover:text-text-primary hover:bg-bg-hover rounded-md transition"
              title="Close (Esc)"
            >
              <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M6 18L18 6M6 6l12 12" />
              </svg>
            </button>
          </div>
        </div>

        {/* Content */}
        <div className="flex-1 overflow-hidden">
          {renderContent()}
        </div>
      </div>
    </>
  );
};

export default ContentPanel;
